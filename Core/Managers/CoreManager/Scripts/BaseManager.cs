using System;
using UnityEngine;
using System.Data;
using Mono.Data.Sqlite;
using System.IO;
using wovencode;

namespace wovencode
{
	
	// ===================================================================================
	// BASE MANAGER
	// ===================================================================================
	public abstract partial class BaseManager : MonoBehaviour
	{
		
		[Header("[BASE OPTIONS]")]
		public bool 	debugMode 		= false;
		
		// -------------------------------------------------------------------------------
		// DebugLog
		// -------------------------------------------------------------------------------
		protected void DebugLog(string sMessage)
		{
			if (debugMode)
				Debug.Log(sMessage);
		}
		
		// -------------------------------------------------------------------------------
		// DebugLogError
		// -------------------------------------------------------------------------------
		protected void DebugLogError(string sMessage)
		{
			if (debugMode)
				Debug.LogError(sMessage);
		}
		
		// =================================== EVENTS ====================================
		
		// -------------------------------------------------------------------------------
		// OnChanged
		// -------------------------------------------------------------------------------
		public abstract void OnChanged();
		
		// -------------------------------------------------------------------------------
		// OnUpdate
		// -------------------------------------------------------------------------------
		public abstract void OnUpdate();
		
		// -------------------------------------------------------------------------------
		// OnRefresh
		// -------------------------------------------------------------------------------
		public abstract void OnRefresh();
		
		// -------------------------------------------------------------------------------
		// OnReset
		// -------------------------------------------------------------------------------
		public abstract void OnReset();
		
		// -------------------------------------------------------------------------------
		// OnLoad
		// -------------------------------------------------------------------------------
		public abstract void OnLoad();
		
		// -------------------------------------------------------------------------------
		// OnSave
		// -------------------------------------------------------------------------------
		public abstract void OnSave();
		
		// ===============================================================================
		
	}
}
